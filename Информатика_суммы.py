
for n in range(1000, 10000):
    # Преобразуем число n в строку, чтобы обращаться к отдельным цифрам по индексам
    sn = str(n)
    # Вычисляем сумму первой и третьей цифры (индексы 0 и 2)
    x = int(sn[0]) + int(sn[2])
    # Вычисляем сумму второй и четвёртой цифры (индексы 1 и 3)
    y = int(sn[1]) + int(sn[3])
    # Формируем новое число: сначала большая сумма, затем меньшая, без разделителей
    r = str(max(x, y)) + str(min(x, y))
    # Проверяем, равно ли полученное число 1712
    if r == "1613":
        # Если условие выполнено, выводим найденное число и завершаем цикл
        print(n)
        break
